        self.assertIn("向其他agent 发送消息或回复时", worker_prompt)
        self.assertIn("收到来自用户或父agent 的新消息", worker_prompt)
        self.assertIn("其他非父级agent 的消息", worker_prompt)
        self.assertIn("child_agent_id", worker_prompt)
        self.assertIn("不要调用 `finish`", worker_prompt)
        self.assertIn("必须通过工具调用收尾", worker_prompt)
        self.assertIn("创建subagent 时必须精确划分范围", worker_prompt)
        self.assertIn("其能做什么、不能做什么", worker_prompt)
        self.assertIn("若未明确说明可修改", worker_prompt)
        self.assertIn("不得修改或删除", worker_prompt)
        self.assertIn("不得自作主张额外添加", worker_prompt)
        self.assertIn("必须严格遵循指令中定义的任务与行动范围及约束", worker_prompt)
        self.assertIn("任务依赖指令中提到的其他agent", worker_prompt)
        self.assertIn("依赖尚未完成且对应agent已终止", worker_prompt)
        self.assertIn("不要把执行范围重叠的任务分配给多个你创建的subagent", worker_prompt)
