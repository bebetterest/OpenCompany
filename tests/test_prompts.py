from __future__ import annotations

import unittest

from opencompany.prompts import PromptLibrary, default_prompts_dir


class PromptLibraryTests(unittest.TestCase):
    @classmethod
    def setUpClass(cls) -> None:
        cls.library = PromptLibrary(default_prompts_dir())

    def test_runtime_message_keys_match_across_locales(self) -> None:
        english = self.library.load_runtime_messages("en")
        chinese = self.library.load_runtime_messages("zh")

        self.assertEqual(set(english), set(chinese))

    def test_root_prompt_clearly_defines_boundaries(self) -> None:
        prompt = self.library.load_agent_prompt("root", "en")

        self.assertIn("Role:", prompt)
        self.assertIn("Context in this conversation:", prompt)
        self.assertIn("including shell", prompt)
        self.assertIn("list_tool_runs", prompt)
        self.assertIn("steer_agent", prompt)
        self.assertIn("send a message or reply to another agent", prompt)
        self.assertIn("new message from the user", prompt)
        self.assertIn("message originating from another agent", prompt)
        self.assertIn("child_agent_id", prompt)
        self.assertIn("multiple tool calls", prompt)
        self.assertIn("Do not call `finish` while unfinished child agents still exist.", prompt)
        self.assertIn("Call `finish` exactly once", prompt)
        self.assertIn("always terminate via a tool call", prompt)
        self.assertIn("define scope boundaries precisely", prompt)
        self.assertIn("what it must not do", prompt)
        self.assertIn("if modification permission is not explicitly granted", prompt)
        self.assertIn("do not modify or delete them", prompt)
        self.assertIn("Never assign overlapping execution scopes", prompt)
        self.assertIn("include the related `child_agent_id`", prompt)
        self.assertIn("do not execute those delegated scopes at root", prompt)
        self.assertIn("If you terminate a child, also terminate other active children that depend", prompt)
        self.assertIn("use `wait_time` or `wait_run`", prompt)
        self.assertIn("Every ended agent should have called `finish`", prompt)
        self.assertIn("inspect the last message", prompt)
        self.assertIn("estimated effort exceeds 24 hours", prompt)

    def test_worker_prompt_clearly_defines_workspace_and_completion_rules(self) -> None:
        prompt = self.library.load_agent_prompt("worker", "en")

        self.assertIn("Role:", prompt)
        self.assertIn("Context in this conversation:", prompt)
        self.assertIn("Work only inside your assigned workspace.", prompt)
        self.assertIn("Prefer decomposition and subagent collaboration;", prompt)
        self.assertIn("list_tool_runs", prompt)
        self.assertIn("steer_agent", prompt)
        self.assertIn("send a message or reply to another agent", prompt)
        self.assertIn("new message from the user or your parent agent", prompt)
        self.assertIn("another non-parent agent", prompt)
        self.assertIn("child_agent_id", prompt)
        self.assertIn("Call `finish` exactly once", prompt)
        self.assertIn("always terminate via a tool call", prompt)
        self.assertIn("Strictly follow instruction-defined task/action scope and constraints.", prompt)
        self.assertIn("define scope boundaries precisely", prompt)
        self.assertIn("what it must not do", prompt)
        self.assertIn("if modification permission is not explicitly granted", prompt)
        self.assertIn("do not modify or delete them", prompt)
        self.assertIn("must not self-initiate extra additions", prompt)
        self.assertIn("references other agents as dependencies", prompt)
        self.assertIn("missing dependency output after dependent agent termination", prompt)
        self.assertIn("Never assign overlapping execution scopes to multiple child agents you create.", prompt)
        self.assertIn("include the responsible `child_agent_id`", prompt)
        self.assertIn("do not execute those delegated scopes yourself in parallel", prompt)
        self.assertIn("also terminate active children that depend on that child's output", prompt)
        self.assertIn("use `wait_time` or `wait_run`", prompt)
        self.assertIn("Every ended agent should have called `finish`", prompt)
        self.assertIn("inspect the last message", prompt)
        self.assertIn("estimated effort exceeds 24 hours", prompt)

